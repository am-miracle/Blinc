//! Container crate for Blinc framework examples.
//!
//! The examples themselves live under `examples/*.rs` and are runnable via:
//!
//! ```sh
//! cargo run -p blinc_app_examples --example canvas_demo
//! ```
//!
//! This crate is `publish = false` so example assets (videos, 3D models, HDRIs)
//! don't bloat the `blinc_app` crate published to crates.io.
